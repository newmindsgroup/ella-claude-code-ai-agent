#!/usr/bin/env bash
# install-capabilities.sh — install EVERY agent capability for a tenant.
#
# This is the step that turns "files copied" into "fully working agent." It
# installs the things bootstrap-tenant.sh used to skip: Graphify, the MCP
# servers, agency-agents sub-agents, Firecrawl, the memory-v2 embedding daemon,
# the Obsidian export crontab, Mission Control, and ALL systemd timers — then
# (optionally) OpenSwarm.
#
# IDEMPOTENT + RE-RUNNABLE. Every step checks current state before mutating and
# is non-fatal on its own, so you can run this against a half-finished deploy to
# complete it, or re-run after `claude login` to finish the auth-dependent bits.
#
# Run as root on the VPS, AFTER bootstrap-tenant.sh has rendered + copied files:
#   sudo bash vps-setup/scripts/install-capabilities.sh vps-setup/tenants/<id>.yml
#
# What each capability needs:
#   - Graphify / MCP servers / agency-agents / Firecrawl  → claude CLI present
#   - Superpowers plugin                                  → claude login (re-run after)
#   - Embedding daemon                                    → python3 + pip
#   - Mission Control                                     → python3 + fastapi/uvicorn
set -uo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
TENANT_FILE="${1:-}"

[[ -z "$TENANT_FILE" ]] && { echo "usage: $0 <path-to-tenant.yml>" >&2; exit 1; }
[[ ! -f "$TENANT_FILE" ]] && { echo "tenant file not found: $TENANT_FILE" >&2; exit 1; }
[[ "$EUID" -ne 0 ]] && { echo "ERROR: must run as root (systemd + crontab + sudoers)" >&2; exit 1; }

python3 -c "import yaml" 2>/dev/null || pip install pyyaml --break-system-packages --quiet

read_yaml() { python3 -c "import yaml; print(yaml.safe_load(open('$TENANT_FILE')).get('$1','') or '')"; }
read_flag() { python3 -c "import yaml; print((yaml.safe_load(open('$TENANT_FILE')).get('features') or {}).get('$1',''))"; }

TENANT_ID=$(read_yaml tenant_id)
LINUX_USER=$(read_yaml linux_user); LINUX_USER="${LINUX_USER:-$TENANT_ID}"
USER_HOME=$(read_yaml user_home);   USER_HOME="${USER_HOME:-/opt/$LINUX_USER}"
AGENT_HOME=$(read_yaml agent_home); AGENT_HOME="${AGENT_HOME:-$USER_HOME/agents}"
RENDERED_DIR="$REPO_ROOT/vps-setup/agents-config/$TENANT_ID"

PASS=0; SKIP=0; WARN=0
ok()   { echo "  ✓ $1"; PASS=$((PASS+1)); }
skip() { echo "  - $1"; SKIP=$((SKIP+1)); }
warn() { echo "  ⚠ $1"; WARN=$((WARN+1)); }
section() { echo; echo "═══ $1 ═══"; }
asuser() { sudo -u "$LINUX_USER" -H bash -lc "$1"; }

echo "=== install-capabilities for tenant: $TENANT_ID ==="
echo "  linux_user: $LINUX_USER   agent_home: $AGENT_HOME"

# ── 1. agent-stack installers (Graphify, MCP servers, agency-agents, Firecrawl, Superpowers) ──
section "1. agent-stack capabilities (Graphify / MCP / agency-agents / Firecrawl / Superpowers)"
CLIENT_ENV="$REPO_ROOT/agent-stack/config/client.env"
if [[ ! -f "$CLIENT_ENV" ]]; then
  mkdir -p "$(dirname "$CLIENT_ENV")"
  cat > "$CLIENT_ENV" <<EOF
# Auto-generated by install-capabilities.sh for tenant $TENANT_ID
CLIENT_NAME="$TENANT_ID"
CLAUDE_PROJECT_ROOT="$AGENT_HOME"
AGENT_MCP_JSON_PATH="$AGENT_HOME/.mcp.json"
EOF
  ok "generated agent-stack/config/client.env"
else
  skip "agent-stack/config/client.env already exists"
fi
chown "$LINUX_USER:$LINUX_USER" "$CLIENT_ENV" 2>/dev/null || true

# Run installers individually, each non-fatal, as the tenant user (installs into
# their ~/.claude). Order matters: superpowers first, graphify last.
for inst in 01-install-superpowers 02-install-mcp-memory 03-install-mcp-fetch \
            04-install-mcp-filesystem 05-install-mcp-playwright 06-install-mcp-chroma \
            07-install-agency-agents 08-install-firecrawl-mcp 09-install-graphify; do
  script="$REPO_ROOT/agent-stack/scripts/$inst.sh"
  if [[ ! -f "$script" ]]; then warn "$inst.sh missing — skipping"; continue; fi
  echo "  → $inst …"
  if asuser "bash '$script'" >/tmp/cap-$inst.log 2>&1; then
    ok "$inst"
  else
    warn "$inst failed (see /tmp/cap-$inst.log) — non-fatal, re-run after 'claude login' if auth-related"
  fi
done

# Verify Graphify specifically (the symptom that triggered this)
if asuser "command -v graphify >/dev/null 2>&1 || [[ -x \$HOME/.local/bin/graphify ]]"; then
  ok "graphify CLI present"
else
  warn "graphify CLI NOT found — check /tmp/cap-09-install-graphify.log"
fi

# ── 2. Memory layer v2 (SQLite vault + embedding daemon) ──
section "2. Memory layer v2 (vault + embedding daemon)"
if [[ "$(read_flag memory_vault_v2)" != "False" ]]; then
  asuser "pip3 install --user --break-system-packages --quiet sentence-transformers numpy" \
    && ok "sentence-transformers + numpy installed" \
    || warn "pip install sentence-transformers failed — semantic recall degraded"
  [[ -f "$AGENT_HOME/scripts/memory-vault.sh" ]] && asuser "bash '$AGENT_HOME/scripts/memory-vault.sh' rebuild" >/dev/null 2>&1 \
    && ok "memory vault rebuilt" || warn "memory-vault.sh rebuild skipped/failed"
  [[ -f "$AGENT_HOME/scripts/install-memory-timers.sh" ]] && bash "$AGENT_HOME/scripts/install-memory-timers.sh" >/dev/null 2>&1 \
    && ok "memory-extract + memory-consolidate timers installed" || skip "install-memory-timers.sh not present"
  [[ -f "$AGENT_HOME/scripts/start-embedding-daemon.sh" ]] && asuser "bash '$AGENT_HOME/scripts/start-embedding-daemon.sh'" >/dev/null 2>&1 \
    && ok "embedding daemon started" || warn "embedding daemon not started (will lazy-start on first recall)"
else
  skip "memory_vault_v2 disabled in tenant.yml"
fi

# ── 3. Obsidian vault mirror (crontab: memory-export every 5 min + @reboot daemons) ──
section "3. Obsidian mirror + high-frequency crontab"
if [[ -f "$AGENT_HOME/crontab/tenant.crontab" ]]; then
  crontab -u "$LINUX_USER" "$AGENT_HOME/crontab/tenant.crontab" \
    && ok "crontab installed (memory-export 5min, entity-linker, commitment-watcher, @reboot daemons)" \
    || warn "crontab install failed"
else
  skip "no crontab/tenant.crontab in render"
fi
if [[ "$(read_flag obsidian_vault_mirror)" != "False" && -f "$AGENT_HOME/scripts/memory-export.py" ]]; then
  asuser "python3 '$AGENT_HOME/scripts/memory-export.py'" >/dev/null 2>&1 \
    && ok "Obsidian vault first export done" || warn "memory-export.py first run failed (cron will retry)"
else
  skip "obsidian_vault_mirror disabled or memory-export.py missing"
fi

# ── 4. Enable ALL systemd timers (bootstrap only enabled 4) ──
section "4. Enable all systemd timers"
enabled=0
if [[ -d "$RENDERED_DIR/systemd" ]]; then
  for unit in "$RENDERED_DIR"/systemd/*.timer; do
    [[ -e "$unit" ]] || continue
    base="$(basename "$unit")"
    if systemctl enable --now "$base" >/dev/null 2>&1; then enabled=$((enabled+1)); fi
  done
  ok "$enabled timers enabled"
else
  warn "no rendered systemd dir at $RENDERED_DIR/systemd — enabling from /etc/systemd/system"
  for unit in /etc/systemd/system/*.timer; do
    base="$(basename "$unit")"
    systemctl enable --now "$base" >/dev/null 2>&1 && enabled=$((enabled+1))
  done
  ok "$enabled timers enabled (from /etc)"
fi

# ── 5. Mission Control (FastAPI backend + observability services) ──
section "5. Mission Control observability stack"
if [[ "$(read_flag mission_control_v0_7)" != "False" && -f "$AGENT_HOME/scripts/bootstrap-mission-control.sh" ]]; then
  bash "$AGENT_HOME/scripts/bootstrap-mission-control.sh" >/tmp/cap-mission-control.log 2>&1 \
    && ok "Mission Control bootstrapped (dashboard-chat + rules + anomaly + session-parser)" \
    || warn "bootstrap-mission-control.sh had errors (see /tmp/cap-mission-control.log)"
else
  skip "mission_control disabled or bootstrap-mission-control.sh missing"
fi

# ── 6. OpenSwarm (optional, heavy) ──
section "6. OpenSwarm multi-agent framework (optional)"
if [[ "$(read_flag multi_agent_swarms)" == "True" ]]; then
  if [[ -f "$REPO_ROOT/installers/openswarm/install-openswarm.sh" ]]; then
    bash "$REPO_ROOT/installers/openswarm/install-openswarm.sh" "$LINUX_USER" >/tmp/cap-openswarm.log 2>&1 \
      && ok "OpenSwarm installed" || warn "OpenSwarm install failed (see /tmp/cap-openswarm.log)"
  else
    warn "multi_agent_swarms enabled but installer missing"
  fi
else
  skip "multi_agent_swarms disabled — built-in swarms still work via swarm-router.sh"
fi

# ── 6b. Telegram (apply full config if the bot token is present) ──
section "6b. Telegram — full config (command menu, allowlist, identity)"
TG_ENV="$USER_HOME/.claude/channels/telegram/.env"
if [[ -f "$AGENT_HOME/scripts/setup-telegram.sh" ]]; then
  if [[ -f "$TG_ENV" ]] && grep -q '^TELEGRAM_BOT_TOKEN=' "$TG_ENV" 2>/dev/null; then
    asuser "bash '$AGENT_HOME/scripts/setup-telegram.sh'" >/tmp/cap-telegram.log 2>&1 \
      && ok "Telegram configured (command menu + description + allowlist applied)" \
      || warn "setup-telegram.sh had issues (see /tmp/cap-telegram.log)"
  else
    skip "no bot token yet — after @BotFather, run: setup-telegram.sh --token <T> --owner-id <ID>"
  fi
else
  skip "setup-telegram.sh not in render"
fi

# ── 7. Discord (optional, needs tokens) ──
section "7. Discord command center (optional)"
if [[ "$(read_flag discord_enabled)" == "True" ]]; then
  if [[ -f "$AGENT_HOME/.env.discord" ]] || [[ -f "$AGENT_HOME/.env" ]] && grep -q DISCORD_BOT_TOKEN "$AGENT_HOME/.env" 2>/dev/null; then
    systemctl enable --now discord-webhook-server.service >/dev/null 2>&1 \
      && ok "Discord webhook server enabled" || warn "discord-webhook-server.service not enabled"
  else
    warn "discord_enabled but no bot token found — follow vps-setup/runbooks/discord-setup.md"
  fi
else
  skip "discord_enabled false"
fi

# ── FINAL ──
echo
echo "═══════════════════════════════════════════════════════════════════"
echo " CAPABILITIES: $PASS ok · $SKIP skipped · $WARN warnings"
echo "═══════════════════════════════════════════════════════════════════"
if [[ $WARN -gt 0 ]]; then
  echo " Some steps warned. Most are auth-dependent (Superpowers, MCP servers"
  echo " that need 'claude login'). Re-run this script after authenticating:"
  echo "   sudo bash $0 $TENANT_FILE"
fi
echo
echo " Verify the full deploy with:"
echo "   bash $REPO_ROOT/vps-setup/scripts/post-deploy-verify.sh   (from your Mac, against the VPS)"
echo "   sudo -u $LINUX_USER bash $AGENT_HOME/scripts/smoke-test.sh   (on the VPS)"
